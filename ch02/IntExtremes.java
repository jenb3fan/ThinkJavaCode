public class IntExtremes
{
    public static void main(String[] args)

    {
        int positiveInt;
        positiveInt = 2147483647;

        System.out.println("Positive Int is: " + positiveInt);

        System.out.print("Positive Int Increase by 1: ");
        System.out.println(positiveInt + 1);

        int negativeInt;
        negativeInt = -2147483648;

        System.out.print("Negative Int Decrease by 1: ");
        System.out.println(negativeInt - 1);



    }
}
